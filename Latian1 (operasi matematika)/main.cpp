#include <iostream>

using namespace std;

int main()
{
    int a,b;
    cout << "Masukkan angka a : ";
    cin >> a;
    cout << "Masukkan angka b : ";
    cin >> b;
    cout << endl;
    cout << "Perkalian" << endl;
    cout << a << "*" << b << "=" << a*b << endl << endl;
    cout << "Pembagian" << endl;
    cout << a << "/" << b << "=" << a/b << endl << endl;
    cout << "Penjumlahan" << endl;
    cout << a << "+" << b << "=" << a+b << endl << endl;
    cout << "Pengurangan" << endl;
    cout << a << "-" << b << "=" << a-b << endl << endl;
    return 0;
}
